# Arithmetic Operators

num1 = 4
num2 = 2
# Addition
sum_result = num1 + num2

# Subtraction 
difference = num1 - num2

# Division
division_result = num1 / num2

# Multiplication
product = num1 * num2

#Modulus
remainder = num1 % num2

#Exponentiation - num1 raise to the power num2
exponent_result = num1 **num2 

#Floor Division - divide and round up result to the nearest whole number
#example 5//2 = 3. so ideally is 2.5, nearing whole numnber is 3.

print("Sum of num1 and num2 is: ",sum_result)
print("Difference of num1 and num2 is: ",difference)
print("Division of num1 and num2 is: ",division_result)
print("Product of num1 and num2 is: ",product)
print("Remainder of num1 and num2 is: ",remainder)
print( "Exponent result of num1 and num2 is: ", exponent_result)



